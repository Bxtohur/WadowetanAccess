    <h1 class="text-2xl font-bold mb-4 text-left">Potensi Ekonomi dan Pembangunan Berkelanjutan</h1>
    <p class="mb-4 text-lg">
      Desa Wado Wetan, sebuah komunitas yang terletak di wilayah pedesaan, menawarkan berbagai potensi signifikan untuk
      pertumbuhan ekonomi dan kesejahteraan masyarakatnya. Dengan mayoritas penduduknya yang berprofesi sebagai petani,
      desa ini menonjol dengan produksi utama seperti padi, sayur, bako, dan bonteng. Meskipun demikian, pertanian di
      sini menghadapi tantangan seperti kurangnya regenerasi dalam pertanian dan kesulitan akses terhadap pupuk. Namun,
      sektor pertanian tetap menjadi tulang punggung ekonomi desa ini, menyediakan lapangan kerja dan menopang mata
      pencaharian mayoritas warga.
    </p>
    <p class="mb-4 text-lg">
      Selain pertanian, Desa Wado Wetan juga menunjukkan pertumbuhan pesat dalam sektor UMKM. Berbagai usaha mikro
      seperti pangsit, baso, gorengan, dan seblak telah berkembang di sini. Meskipun sebagian usaha telah memiliki izin
      usaha, belum semua UMKM memegang sertifikat halal. Pemasaran produk UMKM masih dominan secara tradisional, namun
      beberapa di antaranya mulai memanfaatkan platform digital seperti media sosial untuk meningkatkan jangkauan pasar
      mereka.
    </p>
    <p class="mb-4 text-lg">
      Potensi lain yang dapat ditemui di Desa Wado Wetan adalah industri peternakan dan perikanan. Warga desa ini juga
      aktif dalam beternak sapi, kambing, ayam, serta melakukan usaha perikanan meskipun dalam skala terbatas. Meskipun
      begitu, pengelolaan kehutanan di desa ini masih perlu ditingkatkan, walaupun terdapat peluang untuk memanfaatkan
      sumber daya alam seperti produksi gula merah dan bambu.
    </p>
    <p class="mb-4 text-lg">
      Dalam bidang pendidikan, Desa Wado Wetan dilengkapi dengan beberapa sekolah dasar (SD) dan pesantren. Namun, akses
      terhadap teknologi informasi masih terbatas terutama di lingkungan pesantren. Sarana literasi dan pembacaan juga
      perlu ditingkatkan untuk mendukung perkembangan pendidikan dan pengetahuan masyarakat desa.
    </p>
    <p class="mb-4 text-lg">
      Infrastruktur dan komunikasi di Desa Wado Wetan telah mengalami perkembangan yang signifikan, dengan sinyal
      telekomunikasi yang memadai bahkan di beberapa area telah tersedia akses WiFi. Meskipun demikian, masih diperlukan
      investasi lebih lanjut untuk memperbaiki infrastruktur yang ada guna mendukung aktivitas ekonomi dan sosial
      masyarakat.
    </p>
    <p class="mb-4 text-lg">
      Terakhir, meskipun belum sepenuhnya terwujud, desa ini memiliki potensi pariwisata yang menarik dengan rencana
      pengembangan tempat wisata seperti arena motor cross dan tempat camping. Pengembangan potensi pariwisata dapat
      menjadi salah satu strategi untuk diversifikasi ekonomi desa dan meningkatkan pendapatan masyarakat.
    </p>
    <p class="mb-4 text-lg">
      Secara keseluruhan, Desa Wado Wetan menawarkan peluang besar untuk pertumbuhan ekonomi dan pembangunan
      berkelanjutan. Namun, perlu adanya kerja sama antara pemerintah, masyarakat, dan sektor swasta untuk
      mengoptimalkan potensi yang ada demi kesejahteraan dan kemajuan bersama.
    </p>

